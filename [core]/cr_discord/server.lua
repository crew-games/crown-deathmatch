WebhookList = {}
WebhookDebug = false

WebhookClass = setmetatable({
    constructor = function(self, args)
        self.username = Webhooks[args].username
        self.link = Webhooks[args].link
        self.avatar = Webhooks[args].avatar
        self.public = Webhooks[args].public

        if WebhookDebug then
            outputDebugString("[Webhook] Created channel '" .. args .. "'.")
        end

        return self
    end,

    send = function(self, message)
		local time = getRealTime()
		local year = time.year + 1900
		local month = time.month + 1
		local day = time.monthday
		local hour = time.hour
		local minute = time.minute
		local second = time.second
		local currentDatetime = string.format("%04d-%02d-%02d %02d:%02d:%02d", year, month, day, hour, minute, second)
		
        local sendOptions = {
            connectionAttempts = 3,
            connectTimeout = 5000,
            formFields = {
                content = (self.public and "" or "[" .. currentDatetime .. "] ") .. message:gsub("@everyone", ""):gsub("@here", ""),
                username = self.username,
                avatar_url = self.avatar,
            }
        }

        fetchRemote(self.link, sendOptions, function(responseData)
            if WebhookDebug then
                outputDebugString("[Webhook] " .. responseData)
            end
        end)
    end
}, {
    __call = function(cls, ...)
        local self = {}
        setmetatable(self, { __index = cls })
        self:constructor(...)
        return self
    end
})

addEventHandler("onResourceStart", resourceRoot, function()
    for name, data in pairs(Webhooks) do
        WebhookList[name] = WebhookClass(name)
    end
end)

function sendMessage(channel, message)
    if WebhookList[channel] then
        WebhookList[channel]:send(message)
        if WebhookDebug then
            outputDebugString("[Webhook] Send message '" .. message .. "' from '" .. channel .. "' channel.")
        end
    else
        outputDebugString("[Webhook] Couldn't find the Discord Webhook Channel. (" .. channel .. ")")
    end
end